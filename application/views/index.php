<h1>Dark Dante</h1>
<div class="htimeauth">26:00 | 03/17/2006 | <a href="#">author</a></div>
<p>His best-appreciated hack was a takeover of all of the telephone lines for Los Angeles radio station KIIS-FM, guaranteeing that he would be the 102nd caller, and netting him a Porsche 944 S2.
When the Federal Bureau of Investigation started pursuing Poulsen, he went underground as a fugitive. When he was featured on NBC's Unsolved Mysteries, <strong>the show's 1-800 telephone lines mysteriously crashed</strong>. He was finally arrested in April, 1991. In June 1994, <a href="http://en.wikipedia.org/wiki/Kevin_Poulsen">Poulsen</a> pleaded guilty to seven counts of mail, wire and computer fraud, money laundering, and obstruction of justice, and was sentenced to 51 months in prison and ordered to pay $56,000 in restitution. At the time, it was the longest sentence ever given for hacking. He also pleaded guilty to breaking into computers and obtaining information on undercover businesses run by the FBI.
